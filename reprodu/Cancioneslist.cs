            }
        }

        public string Ubicacion
        {
            get
            {
                return ubicacion;
            }

            set
            {
                ubicacion = value;
            }
        }

        public string Caratula
        {
            get
            {
                return caratula;
            }

            set
            {
                caratula = value;
            }
        }

        public string Letra
        {
            get
            {
                return letra;
            }

            set
            {
                letra = value;
            }
        }

        public string Lista
        {
            get
            {
                return lista;
            }

            set
            {
                lista = value;
            }
        }

    }
}
